const calculadora = {
    sumar: (dato1, dato2) => {
        return dato1 + dato2
    },
    restar: (dato1, dato2) => {
        return dato1 - dato2
    },
    multiplicar: (dato1, dato2) => {
        return dato1 * dato2
    },
    div: (dato1, dato2) => {
        return dato1 / dato2
    },
}

module.exports = calculadora;